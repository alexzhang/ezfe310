#pragma once

// GPIO bit masks for the LEDs and Arduino headers on the HiFive1.

#define GPIO_REDLED		(1 << 22u)
#define GPIO_GREENLED		(1 << 19u)
#define GPIO_BLUELED		(1 << 21u)

#define GPIO_PIN0		(1 << 16u)
#define GPIO_PIN1		(1 << 17u)
#define GPIO_PIN2		(1 << 18u)
#define GPIO_PIN3		(1 << 19u)
#define GPIO_PIN4		(1 << 20u)
#define GPIO_PIN5               (1 << 21u)
#define GPIO_PIN6               (1 << 22u)
#define GPIO_PIN7               (1 << 23u)
#define GPIO_PIN8		(1 << 0u)
#define GPIO_PIN9               (1 << 1u)
#define GPIO_PIN10              (1 << 2u)
#define GPIO_PIN11              (1 << 3u)
#define GPIO_PIN12              (1 << 4u)
#define GPIO_PIN13              (1 << 5u)
// Pin 14 isn't connected.
#define GPIO_PIN15              (1 << 9u)
#define GPIO_PIN16		(1 << 10u)
#define GPIO_PIN17              (1 << 11u)
#define GPIO_PIN18              (1 << 12u)
#define GPIO_PIN19              (1 << 13u)
